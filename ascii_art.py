def get_centered_text(text, width=80):
    """Center text within a given width"""
    lines = text.split('\n')
    centered_lines = []
    for line in lines:
        if line.strip():
            padding = (width - len(line)) // 2
            centered_lines.append(' ' * padding + line)
        else:
            centered_lines.append(line)
    return '\n'.join(centered_lines)
